var nome = "Luiz"
var nome = "Thiago"

// Utilizando o VAR é possível redeclarar uma variável

console.log(nome)

let sobrenome = "Lucas"
sobrenome = "Jorge"

// Utilizando o let não é possível, mas é possível alterar o seu valor, já que ainda sim é uma variável não constante. Portanto, utilize LET e não VAR

console.log(sobrenome)